str1="Md Abdul Awal"
str2="MD ABDUL AWAL"

print("title     : ",str1.title()) #first letter of each word will be capital
print("capitalize: ",str1.capitalize()) # First Letter of each sentence will capital
print("Lower case: ",str2.lower()) # Every Letter of each word will in lower case
print("upper case: ",str2.upper()) # Every Letter of each word will in upper case
print("casefold  : ",str2.casefold()) # Every Letter will in lower case
print("swapcase  : ",str1.swapcase()) # Every Letter will in swapcase

